#include<stdio.h>

int main()
{
    int a[5][5] = 
    {
        {0,0,0,6,0},
        {0,7,0,0,0},
        {0,2,0,0,0},
        {0,0,3,0,0},
        {4,0,0,0,0}
    };

    int b[5][5] =
    {
        {0,0,0,0,0},
        {0,0,0,5,0},
        {0,0,2,0,0},
        {0,0,0,9,0},
        {8,0,0,0,0}
    };

    int c[5][5];


    for(int i = 0; i < 5; i++)
    {
        for(int j = 0; j < 5; j++)
        {
            c[i][j] = a[i][j] + b[i][j];
        }
    }

    for(int i = 0; i < 5; i++)
    {
        for(int j = 0; j < 5; j++)
        {
            printf("%3d ",c[i][j]);
        }
        printf("\n");
    }
    return 0;
}